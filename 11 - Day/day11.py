# Example: Printing numbers from 1 to 5 using a while loop

num = 1

while num <= 5 :
    print (num)    
    num += 1

# Example: Finding the sum of numbers from 1 to 10 using a while loop

num = 1
sum = 0

while num <= 10:
    sum += num
    num += 1
print ("Sum of Numners from 1 to 10 is :", sum)